package org.qubership.atp.itf.lite.backend.feign.dto;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonSubTypes;
import com.fasterxml.jackson.annotation.JsonTypeInfo;
import com.fasterxml.jackson.annotation.JsonTypeName;
import lombok.Data;

@JsonIgnoreProperties(
  value = "type",
  allowSetters = true
)
@JsonTypeInfo(use = JsonTypeInfo.Id.NAME, include = JsonTypeInfo.As.PROPERTY, property = "type", visible = true)
@JsonSubTypes({
  @JsonSubTypes.Type(value = RestLogRecordDto.class, name = "REST")
})
@JsonTypeName("RestLogRecord")
@Data
public class RestLogRecordDto extends LogRecordDto {
  private RequestDto request;
  private ResponseDto response;

  public RestLogRecordDto request(RequestDto request) {
    this.request = request;
    return this;
  }

  public RestLogRecordDto response(ResponseDto response) {
    this.response = response;
    return this;
  }
}

